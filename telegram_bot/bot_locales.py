def get_bot_translation(key: str, language: str = "ru", **kwargs) -> str:
    """
    –ü–æ–ª—É—á–∏—Ç—å –ø–µ—Ä–µ–≤–æ–¥ –¥–ª—è –±–æ—Ç–∞ —Å —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ–º
    Get bot translation with formatting
    
    Args:
        key: –ö–ª—é—á –ø–µ—Ä–µ–≤–æ–¥–∞ / Translation key
        language: –ö–æ–¥ —è–∑—ã–∫–∞ (ru, en) / Language code
        **kwargs: –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è / Formatting parameters
        
    Returns:
        –ü–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ / Translated string
    """
    if language not in BOT_TRANSLATIONS:
        language = "ru"
    
    text = BOT_TRANSLATIONS[language].get(key, key)
    
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    
    return text


